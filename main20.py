import re

# Задание 1. Проверка соответствия пароля.
# Он должен состоять из цифр, букв англ. алфавита, символов дефис, собака и подчеркивание.
# Длина пароля от 6 до 18 символов.
print("Задание 1:")
pass_text = 'my-p@ssw0rd'
# Используем ^ и $ для проверки всей строки от начала до конца
# [a-zA-Z0-9@\-_] — только латиница, цифры и указанные спецсимволы
# {6,18} — длина пароля
regex_pass = r"^[a-zA-Z0-9@\-_]{6,18}$"
match_pass = re.findall(regex_pass, pass_text)

print(f"Пароль: {pass_text}")
print(f"Результат проверки: {match_pass}")
print("-" * 20)


# Задание 2. Найти дату в формате: dd/mm/YYYY
print("Задание 2:")
test_date_text = "В июне 2021 года, 02/06/2021, 05/06/2021, 14/06/2021, были зафиксированы максимумы ежемесячных осадков."
# \d{2} — две цифры дня
# / — разделитель
# \d{2} — две цифры месяца
# / — разделитель
# \d{4} — четыре цифры года
regex_date = r"\d{2}/\d{2}/\d{4}"
dates = re.findall(regex_date, test_date_text)

print(f"Тест: {test_date_text}")
print(f"Результат поиска дат: {dates}")